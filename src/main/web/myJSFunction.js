function loginButton() {
    const dir = document.getElementById(`log`);
    const button = document.getElementById("button1");
    if (dir.style.display === "none" && button.value === "Login") {
        dir.style.display = "block";
        button.value = "Close";
    } else {
        dir.style.display = "none";
        button.value = "Login";
    }
}

